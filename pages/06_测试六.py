# 单选渲染（去除多余label，index=None无默认A）
st.subheader("一、单项选择题（40题×1分=40分）")
sc = 0
for q in single:
    qid = q["id"]
    st.markdown(f"{qid}. {q['title']}")
    opts = q["opt"]
    sel = st.radio("", opts, key=f"s6_{qid}", horizontal=True, index=None)
    user = sel.split(".")[0] if sel is not None else ""
    st.session_state.s6[qid] = user
    with st.expander("查看答案解析", expanded=False):
        st.write(f"标准答案：{q['ans']}")
        st.write(f"解析：{q['ana']}")
        if user == q["ans"]:
            sc += 1
            st.success("作答正确 +1分")
        elif user != "":
            st.error(f"你的选择：{user}，回答错误")
            if qid not in st.session_state.w6["s"]:
                st.session_state.w6["s"].append(qid)
st.session_state.score6 += sc

