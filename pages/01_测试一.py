# 初始化会话状态存储答题、分数数据
def init_session():
    if "single_answer_record" not in st.session_state:
        st.session_state.single_answer_record = {} # 单选作答记录 {题号:选择答案}
    if "multi_answer_record" not in st.session_state:
        st.session_state.multi_answer_record = {} # 多选作答记录 {题号:选择列表}
    if "single_score" not in st.session_state:
        st.session_state.single_score = 0
    if "multi_score" not in st.session_state:
        st.session_state.multi_score = 0
    if "show_result" not in st.session_state:
        st.session_state.show_result = False
    if "wrong_list" not in st.session_state:
        st.session_state.wrong_list = {"single":[], "multi":[]}

init_session()

# ===================== 重置试卷函数 =====================
def reset_exam():
    st.session_state.single_answer_record = {}
    st.session_state.multi_answer_record = {}
    st.session_state.single_score = 0
    st.session_state.multi_score = 0
    st.session_state.show_result = False
    st.session_state.wrong_list = {"single":[], "multi":[]}
    st.rerun()

