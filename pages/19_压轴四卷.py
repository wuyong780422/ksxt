    sel = st.radio("", opts, key=f"s19_{qid}", horizontal=True, index=None)
    user = sel.split(".")[0] if sel is not None else ""
    st.session_state.s19[qid] = user
    with st.expander("查看答案解析", expanded=False):
        st.write(f"标准答案：{q['ans']}")
        st.write(f"解析：{q['ana']}")
        if user == q["ans"]:
            sc += 1
            st.success("作答正确 +1分")
        elif user != "":
            st.error(f"你的选择：{user}，回答错误")
            if qid not in st.session_state.w19["s"]:
                st.session_state.w19["s"].append(qid)
