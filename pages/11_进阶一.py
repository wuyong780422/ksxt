st.subheader("一、单选40题（每题1分）")
sc = 0
for q in single:
    qid = q["id"]
    st.markdown(f"{qid}. {q['title']}")
    opts = q["opt"]
    sel = st.radio("", opts, key=f"s11_{qid}", horizontal=True, index=None)
    user = sel.split(".")[0] if sel is not None else ""
    st.session_state.s11[qid] = user
    with st.expander("解析", expanded=False):
        st.write(f"答案：{q['ans']}｜{q['ana']}")
        if user == q["ans"]:
            sc += 1
            st.success("+1")
        elif user != "":
            st.error(f"你的答案：{user}")
            if qid not in st.session_state.w11["s"]:
                st.session_state.w11["s"].append(qid)
st.session_state.score11 += sc

# 多选
st.divider()
st.subheader("二、多选20题（每题1.5分）")
mc = 0
for q in multi:
    qid = q["id"]
    st.markdown(f"{qid}. {q['title']}")
    opts = q["opt"]
    sel = st.multiselect("", opts, key=f"m11_{qid}", default=[])
    user = [x.split(".")[0] for x in sel]
    st.session_state.m11[qid] = user
    with st.expander("解析", expanded=False):
        std = q["ans"]
        st.write(f"答案：{','.join(std)}｜{q['ana']}")
        if sorted(user) == sorted(std):
            mc += 1.5
            st.success("+1.5")
        elif len(user)>0:
            st.error(f"选择：{','.join(user)}")
            if qid not in st.session_state.w11["m"]:
                st.session_state.w11["m"].append(qid)
st.session_state.score11 += mc

