    opts = q["opt"]
    sel = st.radio("", opts, key=f"s5_{qid}", horizontal=True, index=None)
    user = sel.split(".")[0] if sel is not None else ""
    st.session_state.s5[qid] = user
    with st.expander("查看答案解析", expanded=False):
        st.write(f"标准答案：{q['ans']}")
        st.write(f"解析：{q['ana']}")
