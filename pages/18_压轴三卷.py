# 判断渲染
st.divider()
st.subheader("三、判断题（10题×1分=10分）")
jc = 0
for q in judge:
    qid = q["id"]
    st.markdown(f"{qid}. {q['title']}")
    sel = st.radio("", ["对","错"], key=f"j18_{qid}", horizontal=True, index=None)
    user = sel if sel is not None else ""
    st.session_state.j18[qid] = user
    with st.expander("查看答案解析", expanded=False):
        st.write(f"标准答案：{q['ans']}")
        st.write(f"解析：{q['ana']}")
        if user == q["ans"]:
            jc += 1
            st.success("作答正确 +1分")
        elif user != "":
            st.error(f"你的选择：{user}，回答错误")
            if qid not in st.session_state.j18["j"]:
                st.session_state.j18["j"].append(qid)
st.session_state.score18 += jc

